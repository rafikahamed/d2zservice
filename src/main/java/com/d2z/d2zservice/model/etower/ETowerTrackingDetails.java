package com.d2z.d2zservice.model.etower;

public class ETowerTrackingDetails {

	private String trackingNo;
	private String eventTime;
	private String eventCode;
	private String activity;
	private String location;
	private String referenceTrackingNo;
	private String destCountry;
	private String timeZone;
	public String getTrackingNo() {
		return trackingNo;
	}
	public void setTrackingNo(String trackingNo) {
		this.trackingNo = trackingNo;
	}
	public String getEventTime() {
		return eventTime;
	}
	public void setEventTime(String eventTime) {
		this.eventTime = eventTime;
	}
	public String getEventCode() {
		return eventCode;
	}
	public void setEventCode(String eventCode) {
		this.eventCode = eventCode;
	}
	public String getActivity() {
		return activity;
	}
	public void setActivity(String activity) {
		this.activity = activity;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getReferenceTrackingNo() {
		return referenceTrackingNo;
	}
	public void setReferenceTrackingNo(String referenceTrackingNo) {
		this.referenceTrackingNo = referenceTrackingNo;
	}
	public String getDestCountry() {
		return destCountry;
	}
	public void setDestCountry(String destCountry) {
		this.destCountry = destCountry;
	}
	public String getTimeZone() {
		return timeZone;
	}
	public void setTimeZone(String timeZone) {
		this.timeZone = timeZone;
	}

	
	
	
	
}
